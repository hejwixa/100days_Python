def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}

def calculator():
    print(logo)
    should_work = True
    number_1 = float(input("What is the first number?\n"))

    while should_work:
        for symbol in operations:
            print(symbol)
        operation_symbol = input("Pick an operation\n")
        number_2 = float(input("What is the second number?\n"))
        answer = operations[operation_symbol](number_1, number_2)
        print(f"{number_1} {operation_symbol} {number_2} = {answer}")

        choice = input(f"Type 'y' to continue calculating with {answer}, or type 'n' to start a new calculation:\n").lower()

        if choice == "y":
            number_1 = answer
        else:
            should_work = False
            print("\n" * 20)
            calculator()

calculator()
